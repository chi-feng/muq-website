<h2>Where we come from</h2>
<p> Started internally around 2011, MUQ originally started as an outlet for some of the advanced research going on in Professor Youssef Marzouk's <a href="http://uqgroup.mit.edu/">uncertainty quantification group</a> at MIT.  Since then, we have grown substantially but we still strive to provide a pipeline between cutting-edge algorithmic work and challenging scientific applications.  We are also part of the <a href="http://www.quest-scidac.org/">QUEST</a> SciDAC institute.</p>
